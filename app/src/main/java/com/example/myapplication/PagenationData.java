package com.example.myapplication;

public class PagenationData
{
    private int item_id;
    private String item_proname;
    private String item_proprice;
    private String item_image;

    public int getItem_id() {
        return item_id;
    }

    public void setItem_id(int item_id) {
        this.item_id = item_id;
    }

    public String getItem_proname() {
        return item_proname;
    }

    public void setItem_proname(String item_proname) {
        this.item_proname = item_proname;
    }

    public String getItem_proprice() {
        return item_proprice;
    }

    public void setItem_proprice(String item_proprice) {
        this.item_proprice = item_proprice;
    }

    public String getItem_image() {
        return item_image;
    }

    public void setItem_image(String item_image) {
        this.item_image = item_image;
    }
}
